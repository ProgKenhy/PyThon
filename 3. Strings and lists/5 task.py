def line_check(mat):
    flag = False
    for i in range(len(mat[0]) - 1):
        temp = mat[0][i] / mat[0][i + 1]
        count = 0
        for j in range(1, len(mat)):
            if mat[j][i] / mat[j][i + 1] == temp:
                count += 1
                if count == len(mat) - 1:
                    flag = True
    if flag:
        print("Линейно зависимые")
        for i in mat:
            print(i)
    else:
        print("Линейно независимые")
        for i in mat:
            print(i)


